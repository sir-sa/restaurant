    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $user)
    {
        if(Gate::denies('delete-user')){
            return redirect(route('admin.user.index'));
        }
        $user->roles()->detach();
        $user->delete();
        return redirect()->route('admin.user.index');
